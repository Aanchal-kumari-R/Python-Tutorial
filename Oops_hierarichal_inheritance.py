class parent: 
    def func(self): 
        print("This is a parent class.") 
class child1(parent): 
    def func1(self): 
        print("this is child 1 class.") 
class child2(parent): 
    def func2(parent): 
        print("this is child 2 class.") 
class child3(parent): 
    def func3(parent): 
        print("this is child 3 class.") 
obj1 = child1()
obj2 = child2()
obj3 = child3() 
obj1.func()
obj1.func1()
obj2.func()
obj2.func2()
obj3.func()
obj3.func3()